// Helper functions
export function getCityBySlug(slug: string): CityData | undefined {
  return cities.find(city => city.slug === slug);
}

export function getCitiesByState(stateAbbr: string): CityData[] {
  return cities.filter(city => city.stateAbbr === stateAbbr);
}

export function getHighTrafficCities(): CityData[] {
  return cities.filter(city => city.trafficDensity === "high");
}

export function getCitiesWithMostCrashes(limit: number = 10): CityData[] {
  return [...cities]
    .sort((a, b) => {
      const aNum = parseInt(a.annualMotorcycleCrashes.replace(/[^0-9]/g, ''));
      const bNum = parseInt(b.annualMotorcycleCrashes.replace(/[^0-9]/g, ''));
      return bNum - aNum;
    })
    .slice(0, limit);
}
